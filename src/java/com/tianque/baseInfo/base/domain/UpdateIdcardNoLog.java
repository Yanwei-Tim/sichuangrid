package com.tianque.baseInfo.base.domain;

import java.util.Date;

import org.apache.struts2.json.annotations.JSON;

import com.tianque.core.base.BaseDomain;
import com.tianque.domain.Organization;

/**
 * @Description:修改身份证号码的日志类
 * @author zhangyouwei@hztian.com
 * @date: 2014-7-31 下午10:55:00
 */
public class UpdateIdcardNoLog extends BaseDomain {
	private Organization dataOrg;
	private Long dataId;
	private String dataType;
	private String dataBeforeOperate;
	private String dataAfterOperate;
	private String operateUser;
	private Date operateDate;

	public Organization getDataOrg() {
		return dataOrg;
	}

	public void setDataOrg(Organization dataOrg) {
		this.dataOrg = dataOrg;
	}

	public Long getDataId() {
		return dataId;
	}

	public void setDataId(Long dataId) {
		this.dataId = dataId;
	}

	public String getDataType() {
		return dataType;
	}

	public void setDataType(String dataType) {
		this.dataType = dataType;
	}

	public String getDataBeforeOperate() {
		return dataBeforeOperate;
	}

	public void setDataBeforeOperate(String dataBeforeOperate) {
		this.dataBeforeOperate = dataBeforeOperate;
	}

	public String getDataAfterOperate() {
		return dataAfterOperate;
	}

	public void setDataAfterOperate(String dataAfterOperate) {
		this.dataAfterOperate = dataAfterOperate;
	}

	public String getOperateUser() {
		return operateUser;
	}

	public void setOperateUser(String operateUser) {
		this.operateUser = operateUser;
	}

	@JSON(format = "yyyy-MM-dd")
	public Date getOperateDate() {
		return operateDate;
	}

	public void setOperateDate(Date operateDate) {
		this.operateDate = operateDate;
	}

}

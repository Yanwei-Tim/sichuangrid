package com.tianque.baseInfo.goodSamaritan.domain;

import com.tianque.core.base.BaseDomain;

public class PopulationAttachFile extends BaseDomain {
	/* 人口业务 */
	private Long businessId;
	/* 附件名称 */
	private String fileName;
	/* 附件地址 */
	private String annexAddress;
	/* 人口类型 */
	private String businessType;

	public String getFileName() {
		return fileName;
	}

	public void setFileName(String fileName) {
		this.fileName = fileName;
	}

	public String getAnnexAddress() {
		return annexAddress;
	}

	public void setAnnexAddress(String annexAddress) {
		this.annexAddress = annexAddress;
	}

	public String getBusinessType() {
		return businessType;
	}

	public void setBusinessType(String businessType) {
		this.businessType = businessType;
	}

	public Long getBusinessId() {
		return businessId;
	}

	public void setBusinessId(Long businessId) {
		this.businessId = businessId;
	}
}

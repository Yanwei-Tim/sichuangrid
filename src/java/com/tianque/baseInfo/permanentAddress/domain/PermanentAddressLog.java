package com.tianque.baseInfo.permanentAddress.domain;

import com.tianque.core.base.BaseDomain;

public class PermanentAddressLog extends BaseDomain {

	private String province;
	private String city;
	private String district;
	private Integer addslevel;
	private String addressNo;
	private String changedName;
	private Integer optionType;
	private Integer jobState;

	public String getProvince() {
		return province;
	}

	public void setProvince(String province) {
		this.province = province;
	}

	public String getCity() {
		return city;
	}

	public void setCity(String city) {
		this.city = city;
	}

	public String getDistrict() {
		return district;
	}

	public void setDistrict(String district) {
		this.district = district;
	}

	public Integer getAddslevel() {
		return addslevel;
	}

	public void setAddslevel(Integer addslevel) {
		this.addslevel = addslevel;
	}

	public String getAddressNo() {
		return addressNo;
	}

	public void setAddressNo(String addressNo) {
		this.addressNo = addressNo;
	}

	public String getChangedName() {
		return changedName;
	}

	public void setChangedName(String changedName) {
		this.changedName = changedName;
	}

	public Integer getOptionType() {
		return optionType;
	}

	public void setOptionType(Integer optionType) {
		this.optionType = optionType;
	}

	public Integer getJobState() {
		return jobState;
	}

	public void setJobState(Integer jobState) {
		this.jobState = jobState;
	}

}

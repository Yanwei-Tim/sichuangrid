package com.tianque.baseInfo.houseTrack.domain;

import java.util.Date;

import org.apache.struts2.json.annotations.JSON;

import com.tianque.core.base.BaseDomain;
import com.tianque.domain.Organization;

public class HouseTracks extends BaseDomain {
	private Long houseId;
	private String houseAddress;
	private String houseType;
	private Integer houseinitType;
	private Organization houseOrganization;
	private String houseOrgInternalCode;
	private Organization oldHouseOrganization;
	private Integer operationType;
	private String operationContent;
	private Long operationUserId;
	private Organization operationOrganization;
	private Date operationDate;

	public Long getHouseId() {
		return houseId;
	}

	public void setHouseId(Long houseId) {
		this.houseId = houseId;
	}

	public String getHouseAddress() {
		return houseAddress;
	}

	public void setHouseAddress(String houseAddress) {
		this.houseAddress = houseAddress;
	}

	public String getHouseType() {
		return houseType;
	}

	public void setHouseType(String houseType) {
		this.houseType = houseType;
	}

	public Integer getHouseinitType() {
		return houseinitType;
	}

	public void setHouseinitType(Integer houseinitType) {
		this.houseinitType = houseinitType;
	}

	public Organization getHouseOrganization() {
		return houseOrganization;
	}

	public void setHouseOrganization(Organization houseOrganization) {
		this.houseOrganization = houseOrganization;
	}

	public String getHouseOrgInternalCode() {
		return houseOrgInternalCode;
	}

	public void setHouseOrgInternalCode(String houseOrgInternalCode) {
		this.houseOrgInternalCode = houseOrgInternalCode;
	}

	public Organization getOldHouseOrganization() {
		return oldHouseOrganization;
	}

	public void setOldHouseOrganization(Organization oldHouseOrganization) {
		this.oldHouseOrganization = oldHouseOrganization;
	}

	public Integer getOperationType() {
		return operationType;
	}

	public void setOperationType(Integer operationType) {
		this.operationType = operationType;
	}

	public String getOperationContent() {
		return operationContent;
	}

	public void setOperationContent(String operationContent) {
		this.operationContent = operationContent;
	}

	public Long getOperationUserId() {
		return operationUserId;
	}

	public void setOperationUserId(Long operationUserId) {
		this.operationUserId = operationUserId;
	}

	public Organization getOperationOrganization() {
		return operationOrganization;
	}

	public void setOperationOrganization(Organization operationOrganization) {
		this.operationOrganization = operationOrganization;
	}

	@JSON(format = "yyyy-MM-dd HH:mm:ss")
	public Date getOperationDate() {
		return operationDate;
	}

	public void setOperationDate(Date operationDate) {
		this.operationDate = operationDate;
	}

}
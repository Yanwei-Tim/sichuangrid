package com.tianque.controller.vo;

import java.io.Serializable;
import java.util.List;

import com.tianque.core.vo.ExtTreeData;

public class ExtTreeStringIdData implements Serializable {
	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	protected String cls;
	protected String icon;
	protected String text;
	protected boolean leaf = false;
	protected String id;
	protected boolean expanded = false;
	protected Boolean checked = null;
	protected List<ExtTreeData> children;
	protected int level;

	public ExtTreeStringIdData() {
	}

	public String getCls() {
		return cls;
	}

	public void setCls(String cls) {
		this.cls = cls;
	}

	public String getText() {
		return text;
	}

	public void setText(String text) {
		this.text = text;
	}

	public boolean isLeaf() {
		return leaf;
	}

	public void setLeaf(boolean leaf) {
		this.leaf = leaf;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getIcon() {
		return icon;
	}

	public void setIcon(String icon) {
		this.icon = icon;
	}

	public boolean isExpanded() {
		return expanded;
	}

	public void setExpanded(boolean expanded) {
		this.expanded = expanded;
	}

	public List<ExtTreeData> getChildren() {
		return children;
	}

	public void setChildren(List<ExtTreeData> children) {
		this.children = children;
	}

	public void setChecked(Boolean checked) {
		this.checked = checked;
	}

	public int getLevel() {
		return level;
	}

	public void setLevel(int level) {
		this.level = level;
	}

	public Boolean getChecked() {
		return checked;
	}

}

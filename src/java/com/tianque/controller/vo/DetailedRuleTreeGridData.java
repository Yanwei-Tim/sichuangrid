package com.tianque.controller.vo;

import java.io.Serializable;
import java.util.List;

public class DetailedRuleTreeGridData implements Serializable {
	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	private int treelevel;
	private Long state;
	private Long id;
	private Long parentId;
	private Long ruleType;
	private String summary;
	private Double score;
	private String content;
	private Double standardScore;
	private Double selfAssessmentScore;
	private String remark;
	private boolean leaf;
	private boolean expanded = false;
	private Long[] dialyLogTypes;
	private String scoreReason;
	private List<DailyLogVo> DailyLogs;

	public String getScoreReason() {
		return scoreReason;
	}

	public void setScoreReason(String scoreReason) {
		this.scoreReason = scoreReason;
	}

	public int getTreelevel() {
		return treelevel;
	}

	public void setTreelevel(int treelevel) {
		this.treelevel = treelevel;
	}

	public Long getState() {
		return state;
	}

	public void setState(Long state) {
		this.state = state;
	}

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public Long getParentId() {
		return parentId;
	}

	public void setParentId(Long parentId) {
		this.parentId = parentId;
	}

	public String getSummary() {
		return summary;
	}

	public void setSummary(String summary) {
		this.summary = summary;
	}

	public Double getScore() {
		return score;
	}

	public void setScore(Double score) {
		this.score = score;
	}

	public String getContent() {
		return content;
	}

	public void setContent(String content) {
		this.content = content;
	}

	public Double getStandardScore() {
		return standardScore;
	}

	public void setStandardScore(Double standardScore) {
		this.standardScore = standardScore;
	}

	public String getRemark() {
		return remark;
	}

	public void setRemark(String remark) {
		this.remark = remark;
	}

	public boolean isLeaf() {
		return leaf;
	}

	public void setLeaf(boolean leaf) {
		this.leaf = leaf;
	}

	public boolean isExpanded() {
		// 默认不展开
		expanded = false;
		return expanded;
	}

	public void setExpanded(boolean expanded) {
		this.expanded = expanded;
	}

	public Long[] getDialyLogTypes() {
		return dialyLogTypes;
	}

	public void setDialyLogTypes(Long[] dialyLogTypes) {
		this.dialyLogTypes = dialyLogTypes;
	}

	public Double getSelfAssessmentScore() {
		return selfAssessmentScore;
	}

	public void setSelfAssessmentScore(Double selfAssessmentScore) {
		this.selfAssessmentScore = selfAssessmentScore;
	}

	public Long getRuleType() {
		return ruleType;
	}

	public void setRuleType(Long ruleType) {
		this.ruleType = ruleType;
	}

	public List<DailyLogVo> getDailyLogs() {
		return DailyLogs;
	}

	public void setDailyLogs(List<DailyLogVo> dailyLogs) {
		DailyLogs = dailyLogs;
	}

}

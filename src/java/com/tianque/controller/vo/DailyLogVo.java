package com.tianque.controller.vo;

import java.io.Serializable;

import com.tianque.working.domain.DailyDirectory;

/**
 * 主要用于考核评估
 * 因为日常工作有三个表：
 * workingRecord,significantIssuedeals,keyAreasOfInvestigationInfos
 */
public class DailyLogVo implements Serializable {
	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	private Long id;
	private String name;
	private Long dailyLogType;
	private DailyDirectory dailyDirectory;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public Long getDailyLogType() {
		return dailyLogType;
	}

	public void setDailyLogType(Long dailyLogType) {
		this.dailyLogType = dailyLogType;
	}

	public DailyDirectory getDailyDirectory() {
		return dailyDirectory;
	}

	public void setDailyDirectory(DailyDirectory dailyDirectory) {
		this.dailyDirectory = dailyDirectory;
	}

}

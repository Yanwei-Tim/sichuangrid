package com.tianque.controller.vo;

import com.tianque.core.vo.ExtTreeData;
import com.tianque.working.domain.DailyDirectory;

public class DailyDirectoryTreeVo {
	private ExtTreeData extTreeData;
	private DailyDirectory dailyDirectory;

	public DailyDirectoryTreeVo() {

	}

	public DailyDirectoryTreeVo(ExtTreeData extTreeData, DailyDirectory dailyDirectory) {
		this.extTreeData = extTreeData;
		this.dailyDirectory = dailyDirectory;
	}

	public ExtTreeData getExtTreeData() {
		return extTreeData;
	}

	public void setExtTreeData(ExtTreeData extTreeData) {
		this.extTreeData = extTreeData;
	}

	public DailyDirectory getDailyDirectory() {
		return dailyDirectory;
	}

	public void setDailyDirectory(DailyDirectory dailyDirectory) {
		this.dailyDirectory = dailyDirectory;
	}
}

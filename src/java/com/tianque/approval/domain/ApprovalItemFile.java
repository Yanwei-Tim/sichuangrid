package com.tianque.approval.domain;

import com.tianque.core.base.BaseDomain;

public class ApprovalItemFile extends BaseDomain {

	private ApprovalItem approvalItem;
	private String fileName;
	private String fileActualUrl;

	public ApprovalItem getApprovalItem() {
		return approvalItem;
	}

	public void setApprovalItem(ApprovalItem approvalItem) {
		this.approvalItem = approvalItem;
	}

	public String getFileName() {
		return fileName;
	}

	public void setFileName(String fileName) {
		this.fileName = fileName;
	}

	public String getFileActualUrl() {
		return fileActualUrl;
	}

	public void setFileActualUrl(String fileActualUrl) {
		this.fileActualUrl = fileActualUrl;
	}

}

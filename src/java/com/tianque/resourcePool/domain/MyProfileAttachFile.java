package com.tianque.resourcePool.domain;

import com.tianque.core.base.BaseDomain;

public class MyProfileAttachFile extends BaseDomain {
	/** 我的资料 */
	private MyProfile myPrifile;
	/** 文件名称 */
	private String fileName;
	/** 文件路径 */
	private String fileActualUrl;
	/** 文件大小 */
	private Long fileSize;

	public MyProfile getMyPrifile() {
		return myPrifile;
	}

	public void setMyPrifile(MyProfile myPrifile) {
		this.myPrifile = myPrifile;
	}

	public String getFileName() {
		return fileName;
	}

	public void setFileName(String fileName) {
		this.fileName = fileName;
	}

	public String getFileActualUrl() {
		return fileActualUrl;
	}

	public void setFileActualUrl(String fileActualUrl) {
		this.fileActualUrl = fileActualUrl;
	}

	public Long getFileSize() {
		return fileSize;
	}

	public void setFileSize(Long fileSize) {
		this.fileSize = fileSize;
	}

}

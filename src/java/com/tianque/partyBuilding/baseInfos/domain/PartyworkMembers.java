package com.tianque.partyBuilding.baseInfos.domain;

import com.tianque.domain.Organization;
import com.tianque.domain.PropertyDict;

/**
 * 党工委成员表:实体类(PARTYWORK_MEMBERS)
 * 
 * @author
 * @date 2014-01-14 15:32:52
 */
public class PartyworkMembers extends com.tianque.core.base.BaseDomain implements java.io.Serializable {
	private static final long serialVersionUID = 1L;

	/** 所属网格(ORGID) */
	private Organization organization;
	/** 性别(0男，1女，2未知)(GENDER) */
	private PropertyDict gender;
	/** 排序值(SORT) */
	private Integer sort;
	/** 所属网格编号(ORGCODE) */
	private String orgCode;
	/** 姓名(NAME) */
	private String name;
	/** 职位(DUTY) */
	private String duty;
	/** 图片地址(IMAGEURL) */
	private String imageUrl;
	/** 介绍(INTRODUCTION) */
	private String introduction;
	private String genderName;
	/** 基本信息类型 */
	private String baseInfoType;

	public PartyworkMembers() {

	}

	public PartyworkMembers(Organization organization, PropertyDict gender, Integer sort, String orgCode, String name,
			String duty, String imageUrl, String introduction) {
		this.organization = organization;
		this.gender = gender;
		this.sort = sort;
		this.orgCode = orgCode;
		this.name = name;
		this.duty = duty;
		this.imageUrl = imageUrl;
		this.introduction = introduction;
	}

	/** get 所属网格(orgId) */
	public Organization getOrganization() {
		return organization;
	}

	/** set 所属网格(ORGID) */
	public void setOrganization(Organization organization) {
		this.organization = organization;
	}

	/** get 性别(0男，1女，2未知)(gender) */
	public PropertyDict getGender() {
		return gender;
	}

	/** set 性别(0男，1女，2未知)(GENDER) */
	public void setGender(PropertyDict gender) {
		this.gender = gender;
	}

	/** get 排序值(sort) */
	public Integer getSort() {
		return sort;
	}

	/** set 排序值(SORT) */
	public void setSort(Integer sort) {
		this.sort = sort;
	}

	/** get 所属网格编号(orgCode) */
	public String getOrgCode() {
		return orgCode;
	}

	/** set 所属网格编号(ORGCODE) */
	public void setOrgCode(String orgCode) {
		this.orgCode = orgCode;
	}

	/** get 姓名(name) */
	public String getName() {
		return name;
	}

	/** set 姓名(NAME) */
	public void setName(String name) {
		this.name = name;
	}

	/** get 职位(duty) */
	public String getDuty() {
		return duty;
	}

	/** set 职位(DUTY) */
	public void setDuty(String duty) {
		this.duty = duty;
	}

	/** get 图片地址(imageUrl) */
	public String getImageUrl() {
		return imageUrl;
	}

	/** set 图片地址(IMAGEURL) */
	public void setImageUrl(String imageUrl) {
		this.imageUrl = imageUrl;
	}

	/** get 介绍(introduction) */
	public String getIntroduction() {
		return introduction;
	}

	/** set 介绍(INTRODUCTION) */
	public void setIntroduction(String introduction) {
		this.introduction = introduction;
	}

	public String getGenderName() {
		return genderName;
	}

	public void setGenderName(String genderName) {
		this.genderName = genderName;
	}

	public String getBaseInfoType() {
		return baseInfoType;
	}

	public void setBaseInfoType(String baseInfoType) {
		this.baseInfoType = baseInfoType;
	}

}

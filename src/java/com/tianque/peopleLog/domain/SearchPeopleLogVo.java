package com.tianque.peopleLog.domain;

import java.util.Date;
import java.util.Map;

import com.tianque.core.base.BaseDomain;

public class SearchPeopleLogVo extends BaseDomain {

	/* 日志Id */
	private Long logId;
	/* 日志所属人 */
	private String belonger;
	/* 所属责任区编号 */
	private String orgInternalCode;
	/* 点评人Id */
	private Long userId;
	/* 日志点评封装 */
	private Map<String, String> commentLog;
	/* 地点 */
	private String address;
	/* 日志标题 */
	private String title;
	/* 最少点评数 */
	private Long sumCommentMin;
	/* 最多点评数 */
	private Long sumCommentMax;
	/* 日志时间 从 */
	private Date startDate;
	/* 日志时间 到 */
	private Date endDate;
	/* 点评时间 从 */
	private Date commentStartDate;
	/* 点评时间 到 */
	private Date commentEndDate;
	/* 判断是否本级（只在查询中使用） */
	private boolean isPeer;

	public boolean getIsPeer() {
		return isPeer;
	}

	public void setIsPeer(boolean isPeer) {
		this.isPeer = isPeer;
	}

	public Long getLogId() {
		return logId;
	}

	public void setLogId(Long logId) {
		this.logId = logId;
	}

	public String getBelonger() {
		return belonger;
	}

	public void setBelonger(String belonger) {
		this.belonger = belonger;
	}

	public Long getUserId() {
		return userId;
	}

	public void setUserId(Long userId) {
		this.userId = userId;
	}

	public Map<String, String> getCommentLog() {
		return commentLog;
	}

	public void setCommentLog(Map<String, String> commentLog) {
		this.commentLog = commentLog;
	}

	public String getAddress() {
		return address;
	}

	public void setAddress(String address) {
		this.address = address;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public Long getSumCommentMin() {
		return sumCommentMin;
	}

	public void setSumCommentMin(Long sumCommentMin) {
		this.sumCommentMin = sumCommentMin;
	}

	public Long getSumCommentMax() {
		return sumCommentMax;
	}

	public void setSumCommentMax(Long sumCommentMax) {
		this.sumCommentMax = sumCommentMax;
	}

	public Date getStartDate() {
		return startDate;
	}

	public void setStartDate(Date startDate) {
		this.startDate = startDate;
	}

	public Date getEndDate() {
		return endDate;
	}

	public void setEndDate(Date endDate) {
		this.endDate = endDate;
	}

	public String getOrgInternalCode() {
		return orgInternalCode;
	}

	public void setOrgInternalCode(String orgInternalCode) {
		this.orgInternalCode = orgInternalCode;
	}

	public Date getCommentStartDate() {
		return commentStartDate;
	}

	public void setCommentStartDate(Date commentStartDate) {
		this.commentStartDate = commentStartDate;
	}

	public Date getCommentEndDate() {
		return commentEndDate;
	}

	public void setCommentEndDate(Date commentEndDate) {
		this.commentEndDate = commentEndDate;
	}

}

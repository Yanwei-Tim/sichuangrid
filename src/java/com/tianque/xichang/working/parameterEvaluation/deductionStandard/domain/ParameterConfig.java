package com.tianque.xichang.working.parameterEvaluation.deductionStandard.domain;

import com.tianque.core.base.BaseDomain;

public class ParameterConfig extends BaseDomain implements java.io.Serializable {
	private Double handleScore;
	private Double transferredScore;
	private Double circulationScore;

	public Double getHandleScore() {
		return handleScore;
	}

	public void setHandleScore(Double handleScore) {
		this.handleScore = handleScore;
	}

	public Double getTransferredScore() {
		return transferredScore;
	}

	public void setTransferredScore(Double transferredScore) {
		this.transferredScore = transferredScore;
	}

	public Double getCirculationScore() {
		return circulationScore;
	}

	public void setCirculationScore(Double circulationScore) {
		this.circulationScore = circulationScore;
	}
}

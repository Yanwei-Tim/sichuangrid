	public String getPreviousSteadyInfo() {
		return previousSteadyInfo;
	}

	public void setPreviousSteadyInfo(String previousSteadyInfo) {
		this.previousSteadyInfo = previousSteadyInfo;
	}

	public String getPreviousResolveInfo() {
		return previousResolveInfo;
	}

	public void setPreviousResolveInfo(String previousResolveInfo) {
		this.previousResolveInfo = previousResolveInfo;
	}


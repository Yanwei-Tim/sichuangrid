	/**
	 * 化解责任人
	 */
	private String resolveUser;
	/**
	 * 诉求类别
	 */
	private String aspirationType;
	/**
	 * 涉稳人员或群体稳定现状
	 */
	private String steadyInfo;
	/**
	 * 预警级别
	 */

package com.tianque.issueAbutmentJoint.domain;

/***
 * @Description:事件对接已办结附件类
 * @author zhangyouwei@hztianque.com
 * @date: 2015-3-5 上午09:45:22
 */
public class IssueJointAttachfile {
	private Long id;
	/** 文件名 */
	private String fileName;
	/** 文件路径包含文件名（真实名称） */
	private String physicsFullFileName;
	/** 是新增还是办结 */
	private String moduleKey;
	/** 事件主表id */
	private Long issueId;

	public String getFileName() {
		return fileName;
	}

	public void setFileName(String fileName) {
		this.fileName = fileName;
	}

	public String getPhysicsFullFileName() {
		return physicsFullFileName;
	}

	public void setPhysicsFullFileName(String physicsFullFileName) {
		this.physicsFullFileName = physicsFullFileName;
	}

	public String getModuleKey() {
		return moduleKey;
	}

	public void setModuleKey(String moduleKey) {
		this.moduleKey = moduleKey;
	}

	public Long getIssueId() {
		return issueId;
	}

	public void setIssueId(Long issueId) {
		this.issueId = issueId;
	}

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

}

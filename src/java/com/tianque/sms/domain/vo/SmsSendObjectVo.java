package com.tianque.sms.domain.vo;

import java.util.List;

import com.tianque.domain.Organization;
import com.tianque.sms.domain.SmsSendObject;
import com.tianque.sms.domain.Smsquerycondition;

/**
 * 发送对象:辅助类
 */
public class SmsSendObjectVo extends SmsSendObject {
	/**
	 * 手机
	 */
	private String mobile;
	/**
	 * 所属网格
	 */
	private Organization orgId;
	/**
	 * 所属网格编号
	 */
	private String orgInternalCode;
	/**
	 * 查询条件参考字段集合
	 */
	private List<Smsquerycondition> sqcList;

	public String getMobile() {
		return mobile;
	}

	public void setMobile(String mobile) {
		this.mobile = mobile;
	}

	public Organization getOrgId() {
		return orgId;
	}

	public void setOrgId(Organization orgId) {
		this.orgId = orgId;
	}

	public String getOrgInternalCode() {
		return orgInternalCode;
	}

	public void setOrgInternalCode(String orgInternalCode) {
		this.orgInternalCode = orgInternalCode;
	}

	public List<Smsquerycondition> getSqcList() {
		return sqcList;
	}

	public void setSqcList(List<Smsquerycondition> sqcList) {
		this.sqcList = sqcList;
	}

}

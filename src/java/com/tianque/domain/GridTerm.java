package com.tianque.domain;

import com.tianque.core.base.BaseDomain;

@SuppressWarnings("serial")
public class GridTerm extends BaseDomain {
	private String termNo;

	private String sysDefaultName;

	private String specificName;

	private String moduleName;

	public String getModuleName() {
		return moduleName;
	}

	public void setModuleName(String moduleName) {
		this.moduleName = moduleName;
	}

	public String getSpecificName() {
		return specificName;
	}

	public void setSpecificName(String specificName) {
		this.specificName = specificName;
	}

	public String getTermNo() {
		return termNo;
	}

	public void setTermNo(String termNo) {
		this.termNo = termNo;
	}

	public String getSysDefaultName() {
		return sysDefaultName;
	}

	public void setSysDefaultName(String sysDefaultName) {
		this.sysDefaultName = sysDefaultName;
	}
}

package com.tianque.domain;

import com.tianque.core.base.BaseDomain;

public class OverTimeIssueLog extends BaseDomain {
	private Integer year;
	private Integer month;
	private String orgName;
	private Long orgId;
	private String orgInternalCode;
	private Long issueId;
	private Long issueLogId;
	private Long overType;

	public Integer getYear() {
		return year;
	}

	public void setYear(Integer year) {
		this.year = year;
	}

	public Integer getMonth() {
		return month;
	}

	public void setMonth(Integer month) {
		this.month = month;
	}

	public String getOrgName() {
		return orgName;
	}

	public void setOrgName(String orgName) {
		this.orgName = orgName;
	}

	public Long getOrgId() {
		return orgId;
	}

	public void setOrgId(Long orgId) {
		this.orgId = orgId;
	}

	public String getOrgInternalCode() {
		return orgInternalCode;
	}

	public void setOrgInternalCode(String orgInternalCode) {
		this.orgInternalCode = orgInternalCode;
	}

	public Long getIssueId() {
		return issueId;
	}

	public void setIssueId(Long issueId) {
		this.issueId = issueId;
	}

	public Long getIssueLogId() {
		return issueLogId;
	}

	public void setIssueLogId(Long issueLogId) {
		this.issueLogId = issueLogId;
	}

	public Long getOverType() {
		return overType;
	}

	public void setOverType(Long overType) {
		this.overType = overType;
	}

}

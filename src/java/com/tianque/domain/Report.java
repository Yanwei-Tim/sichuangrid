package com.tianque.domain;

import java.util.Date;

import org.apache.struts2.json.annotations.JSON;

import com.tianque.core.base.BaseDomain;

public class Report extends BaseDomain {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	private String jsonData;
	private ReportType reportType;
	private String year;
	private String month;
	private String title;
	private Organization organization;
	private String orgInternalCode;
	private ReportState reportState;
	private ReportLevel reportLevel;
	private Date reportDate;
	private String reportPerson;
	private String repState;
	private Date createDate;
	private String createRepUser;
	private String version;

	public String getVersion() {
		return version;
	}

	public void setVersion(String version) {
		this.version = version;
	}

	public String getCreateRepUser() {
		return createRepUser;
	}

	public void setCreateRepUser(String createRepUser) {
		this.createRepUser = createRepUser;
	}

	public String getJsonData() {
		return jsonData;
	}

	public void setJsonData(String jsonData) {
		this.jsonData = jsonData;
	}

	public String getYear() {
		return year;
	}

	public void setYear(String year) {
		this.year = year;
	}

	public String getMonth() {
		return month;
	}

	public void setMonth(String month) {
		this.month = month;
	}

	public Organization getOrganization() {
		return organization;
	}

	public void setOrganization(Organization organization) {
		this.organization = organization;
	}

	public String getOrgInternalCode() {
		return orgInternalCode;
	}

	public void setOrgInternalCode(String orgInternalCode) {
		this.orgInternalCode = orgInternalCode;
	}

	public ReportType getReportType() {
		return reportType;
	}

	public void setReportType(ReportType reportType) {
		this.reportType = reportType;
	}

	public ReportState getReportState() {
		return reportState;
	}

	public void setReportState(ReportState reportState) {
		this.reportState = reportState;
	}

	public ReportLevel getReportLevel() {
		return reportLevel;
	}

	public void setReportLevel(ReportLevel reportLevel) {
		this.reportLevel = reportLevel;
	}

	@JSON(format = "yyyy-MM-dd")
	public Date getReportDate() {
		return reportDate;
	}

	public void setReportDate(Date reportDate) {
		this.reportDate = reportDate;
	}

	public String getReportPerson() {
		return reportPerson;
	}

	public void setReportPerson(String reportPerson) {
		this.reportPerson = reportPerson;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getRepState() {
		return repState;
	}

	public void setRepState(String repState) {
		this.repState = repState;
	}

	@JSON(format = "yyyy-MM-dd")
	public Date getCreateDate() {
		return createDate;
	}

	public void setCreateDate(Date createDate) {
		this.createDate = createDate;
	}
}

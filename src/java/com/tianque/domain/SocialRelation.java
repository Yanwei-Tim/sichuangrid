package com.tianque.domain;

import java.util.Date;

import org.apache.struts2.json.annotations.JSON;

import com.tianque.core.base.BaseDomain;

/**
 * 流动人口---社会关系
 */
@SuppressWarnings("serial")
public class SocialRelation extends BaseDomain {
	/** 流动人口id **/
	private Long trampResidentId;
	/** 姓名 */
	private String name;
	/** 关系 **/
	private String relation;
	/** 身份证号 **/
	private String idCardNo;
	/** 性别 */
	private PropertyDict gender;
	/** 别名 */
	private String otherName;
	/** 出生日期 **/
	private Date birthDay;
	/** 联系电话 */
	private String phone;
	/** 从事职业 */
	private PropertyDict occupation;
	/** 常住户口地址 */
	private String residentialAccount;
	/** 常住地址 */
	private String currentAddress;
	/** 工作地点 */
	private String workPlace;

	public Long getTrampResidentId() {
		return trampResidentId;
	}

	public void setTrampResidentId(Long trampResidentId) {
		this.trampResidentId = trampResidentId;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getRelation() {
		return relation;
	}

	public void setRelation(String relation) {
		this.relation = relation;
	}

	public String getIdCardNo() {
		return idCardNo;
	}

	public void setIdCardNo(String idCardNo) {
		this.idCardNo = idCardNo;
	}

	public PropertyDict getGender() {
		return gender;
	}

	public void setGender(PropertyDict gender) {
		this.gender = gender;
	}

	public String getOtherName() {
		return otherName;
	}

	public void setOtherName(String otherName) {
		this.otherName = otherName;
	}

	@JSON(format = "yyyy-MM-dd")
	public Date getBirthDay() {
		return birthDay;
	}

	public void setBirthDay(Date birthDay) {
		this.birthDay = birthDay;
	}

	public String getPhone() {
		return phone;
	}

	public void setPhone(String phone) {
		this.phone = phone;
	}

	public PropertyDict getOccupation() {
		return occupation;
	}

	public void setOccupation(PropertyDict occupation) {
		this.occupation = occupation;
	}

	public String getResidentialAccount() {
		return residentialAccount;
	}

	public void setResidentialAccount(String residentialAccount) {
		this.residentialAccount = residentialAccount;
	}

	public String getCurrentAddress() {
		return currentAddress;
	}

	public void setCurrentAddress(String currentAddress) {
		this.currentAddress = currentAddress;
	}

	public String getWorkPlace() {
		return workPlace;
	}

	public void setWorkPlace(String workPlace) {
		this.workPlace = workPlace;
	}
}

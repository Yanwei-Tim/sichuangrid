package com.tianque.domain;

import java.io.Serializable;

/**
 * @Description:网格化服务管理信息系统使用情况
 * @author zhangyouwei@hztianque.com
 * @date: 2015-3-27 下午05:32:08
 */
public class UsedInfo implements Serializable {

	private static final long serialVersionUID = 7718367921476493632L;

	/** 所在组织机构 */
	private Organization org;
	/** 日登陆账号数 */
	private int dayActive;
	/** 月登陆账号数 */
	private int monthActive;
	/** 周登陆账号数 */
	private int weekActive;
	/** 日账号总数 */
	private int dayAccountSum;
	/** 月账号总数 */
	private int monthAccountSum;
	/** 周账号总数 */
	private int weekAccountSum;
	/** 日活跃度 */
	private String dayActiveRate;
	/** 月活跃度 */
	private String monthActiveRate;
	/** 周活跃度 */
	private String weekActiveRate;
	/** 日服务记录总数 */
	private int daySpecialCrowdCount;
	/** 月服务记录总数 */
	private int monthSpecialCrowdCount;
	/** 周服务记录总数 */
	private int weekSpecialCrowdCount;

	public UsedInfo() {
	}

	public UsedInfo(int dayActive, int weekActive, int monthActive,
			int dayAccountSum, int weekAccountSum, int monthAccountSum,
			int daySpecialCrowdCount, int weekSpecialCrowdCount,
			int monthSpecialCrowdCount) {
		this.dayActive = dayActive;
		this.weekActive = weekActive;
		this.monthActive = monthActive;
		this.dayAccountSum = dayAccountSum;
		this.weekAccountSum = weekAccountSum;
		this.monthAccountSum = monthAccountSum;
		this.daySpecialCrowdCount = daySpecialCrowdCount;
		this.weekSpecialCrowdCount = weekSpecialCrowdCount;
		this.monthSpecialCrowdCount = monthSpecialCrowdCount;
	}

	public Organization getOrg() {
		return org;
	}

	public void setOrg(Organization org) {
		this.org = org;
	}

	public int getDayActive() {
		return dayActive;
	}

	public void setDayActive(int dayActive) {
		this.dayActive = dayActive;
	}

	public int getMonthActive() {
		return monthActive;
	}

	public void setMonthActive(int monthActive) {
		this.monthActive = monthActive;
	}

	public int getWeekActive() {
		return weekActive;
	}

	public void setWeekActive(int weekActive) {
		this.weekActive = weekActive;
	}

	public int getDayAccountSum() {
		return dayAccountSum;
	}

	public void setDayAccountSum(int dayAccountSum) {
		this.dayAccountSum = dayAccountSum;
	}

	public int getMonthAccountSum() {
		return monthAccountSum;
	}

	public void setMonthAccountSum(int monthAccountSum) {
		this.monthAccountSum = monthAccountSum;
	}

	public int getWeekAccountSum() {
		return weekAccountSum;
	}

	public void setWeekAccountSum(int weekAccountSum) {
		this.weekAccountSum = weekAccountSum;
	}

	public String getDayActiveRate() {
		return dayActiveRate;
	}

	public void setDayActiveRate(String dayActiveRate) {
		this.dayActiveRate = dayActiveRate;
	}

	public String getMonthActiveRate() {
		return monthActiveRate;
	}

	public void setMonthActiveRate(String monthActiveRate) {
		this.monthActiveRate = monthActiveRate;
	}

	public String getWeekActiveRate() {
		return weekActiveRate;
	}

	public void setWeekActiveRate(String weekActiveRate) {
		this.weekActiveRate = weekActiveRate;
	}

	public int getDaySpecialCrowdCount() {
		return daySpecialCrowdCount;
	}

	public void setDaySpecialCrowdCount(int daySpecialCrowdCount) {
		this.daySpecialCrowdCount = daySpecialCrowdCount;
	}

	public int getMonthSpecialCrowdCount() {
		return monthSpecialCrowdCount;
	}

	public void setMonthSpecialCrowdCount(int monthSpecialCrowdCount) {
		this.monthSpecialCrowdCount = monthSpecialCrowdCount;
	}

	public int getWeekSpecialCrowdCount() {
		return weekSpecialCrowdCount;
	}

	public void setWeekSpecialCrowdCount(int weekSpecialCrowdCount) {
		this.weekSpecialCrowdCount = weekSpecialCrowdCount;
	}

}

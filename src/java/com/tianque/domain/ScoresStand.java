package com.tianque.domain;

import java.io.Serializable;

import com.tianque.core.base.BaseDomain;

public class ScoresStand extends BaseDomain implements Serializable {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	private Evaluate evaluate;
	private Integer startScore;
	private Integer endScore;
	private PropertyDict propertyDict;
	private String name;
	private int scoresStandardType;

	public int getScoresStandardType() {
		return scoresStandardType;
	}

	public void setScoresStandardType(int scoresStandardType) {
		this.scoresStandardType = scoresStandardType;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public Evaluate getEvaluate() {
		return evaluate;
	}

	public void setEvaluate(Evaluate evaluate) {
		this.evaluate = evaluate;
	}

	public PropertyDict getPropertyDict() {
		return propertyDict;
	}

	public void setPropertyDict(PropertyDict propertyDict) {
		this.propertyDict = propertyDict;
	}

	public static long getSerialversionuid() {
		return serialVersionUID;
	}

	public Integer getStartScore() {
		return startScore;
	}

	public void setStartScore(Integer startScore) {
		this.startScore = startScore;
	}

	public Integer getEndScore() {
		return endScore;
	}

	public void setEndScore(Integer endScore) {
		this.endScore = endScore;
	}

}

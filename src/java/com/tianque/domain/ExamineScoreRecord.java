package com.tianque.domain;

import com.tianque.core.base.BaseDomain;

public class ExamineScoreRecord extends BaseDomain {
	private static final long serialVersionUID = 1L;

	// 年度评分主表Id
	private int examineScoresId;

	// 评分内容
	private String scoreContent;

	// 得分
	private double score;

	// 具体小则
	private ExamineItem examineItem;

	public int getExamineScoresId() {
		return examineScoresId;
	}

	public void setExamineScoresId(int examineScoresId) {
		this.examineScoresId = examineScoresId;
	}

	public String getScoreContent() {
		return scoreContent;
	}

	public void setScoreContent(String scoreContent) {
		this.scoreContent = scoreContent;
	}

	public double getScore() {
		return score;
	}

	public void setScore(double score) {
		this.score = score;
	}

	public ExamineItem getExamineItem() {
		return examineItem;
	}

	public void setExamineItem(ExamineItem examineItem) {
		this.examineItem = examineItem;
	}
}

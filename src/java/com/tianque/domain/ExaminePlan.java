package com.tianque.domain;

import java.util.List;

import com.tianque.core.base.BaseDomain;

public class ExaminePlan extends BaseDomain {
	private static final long serialVersionUID = 1L;

	// 年度考核计划
	private String year;

	// 标题
	private String title;

	// 制定部门
	private String draftOrganization;

	// 包含的细则
	private List<ExamineItem> examineItems;

	// 总分
	private double fullScore;

	public String getYear() {
		return year;
	}

	public void setYear(String year) {
		this.year = year;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getDraftOrganization() {
		return draftOrganization;
	}

	public void setDraftOrganization(String draftOrganization) {
		this.draftOrganization = draftOrganization;
	}

	public List<ExamineItem> getExamineItems() {
		return examineItems;
	}

	public void setExamineItems(List<ExamineItem> examineItems) {
		this.examineItems = examineItems;
	}

	public double getFullScore() {
		return fullScore;
	}

	public void setFullScore(double fullScore) {
		this.fullScore = fullScore;
	}
}

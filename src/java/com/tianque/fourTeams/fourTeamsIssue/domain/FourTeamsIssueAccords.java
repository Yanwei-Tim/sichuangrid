package com.tianque.fourTeams.fourTeamsIssue.domain;

import com.tianque.core.base.BaseDomain;

/**
 * @ClassName: IssueAccord
 * @Description: 考核打分
 * @author wangxiaohu wsmalltiger@163.com
 * @date Nov 25, 2013 7:26:56 PM
 */
public class FourTeamsIssueAccords extends BaseDomain {
	private String score;// 分值
	private String type;// 1 加分 0减分
	private String orgId;// 打分机构
	private String info;// 打分原因
	private String logId;// 日志ID

	public String getScore() {
		return score;
	}

	public void setScore(String score) {
		this.score = score;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getOrgId() {
		return orgId;
	}

	public void setOrgId(String orgId) {
		this.orgId = orgId;
	}

	public String getInfo() {
		return info;
	}

	public void setInfo(String info) {
		this.info = info;
	}

	public String getLogId() {
		return logId;
	}

	public void setLogId(String logId) {
		this.logId = logId;
	}
}

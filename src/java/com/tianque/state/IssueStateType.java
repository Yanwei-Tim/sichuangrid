package com.tianque.state;

public class IssueStateType {
	public static final Integer UN_CONCEPTE = 1;
	public static final Integer PROCESSING = 2;
	public static final Integer COMPLETE = 3;

	public static String toString(int typeValue) {
		switch (typeValue) {
		case 1:
			return "未受理";
		case 2:
			return "处理中";
		case 3:
			return "已完成";
		default:
			return "";
		}
	}
}

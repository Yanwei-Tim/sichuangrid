package com.tianque.state;

public class PersonnelTrackOperationType {
	public static final Integer ATTENTIONED = 1; // 被关注
	public static final Integer CANCEL_ATTENTION = 2; // 取消关注
	public static final Integer AGAIN_ATTENTION = 3; // 重新关注
	public static final Integer MOVE = 4; // 转移
	public static final Integer TO = 5; // 迁入
	public static final Integer FROM = 6; // 迁出
	public static final Integer ADDT = 7;// 录入
	public static final Integer DOOM = 8;// 死亡
	public static final Integer LOGOUT = 9;// 注销
	public static final Integer DELETE = 10;// 删除
	public static final Integer CANCEL_LOGOUT = 11;// 取消注销

	public static String toString(int typeValue) {
		switch (typeValue) {
		case 1:
			return "关注";
		case 2:
			return "取消关注";
		case 3:
			return "重新关注";
		case 4:
			return "转移到";
		case 5:
			return "迁入到";
		case 6:
			return "迁出到";
		case 7:
			return "录入";
		case 8:
			return "死亡";
		case 9:
			return "注销";
		case 10:
			return "删除";
		default:
			return "";
		}
	}
}

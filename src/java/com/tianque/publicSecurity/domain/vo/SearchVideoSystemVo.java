package com.tianque.publicSecurity.domain.vo;

import java.util.Date;

import com.tianque.core.base.BaseDomain;

public class SearchVideoSystemVo extends BaseDomain {

	/** 最大 注销时间 */
	private Date maxLogoutTime;
	/** 最小 注销时间 */
	private Date minLogoutTime;
	/** 所属网格 */
	private Long orgId;
	/** 是否注销 */
	private Long isEmphasis;
	/** 所属网格编号 */
	private String orgInternalCode;

	/** 编号 */
	private String videoNo;
	/** 地址 */
	private String address;

	/** 视频url**/
	private String url;

	/** 账号名**/
	private String account;

	/** 密码**/
	private String password;

	/** 注销原因 */
	private String logoutReason;

	/** 是否绑定 */
	private Boolean isBind;

	public Date getMaxLogoutTime() {
		return maxLogoutTime;
	}

	public void setMaxLogoutTime(Date maxLogoutTime) {
		this.maxLogoutTime = maxLogoutTime;
	}

	public Date getMinLogoutTime() {
		return minLogoutTime;
	}

	public void setMinLogoutTime(Date minLogoutTime) {
		this.minLogoutTime = minLogoutTime;
	}

	public Long getOrgId() {
		return orgId;
	}

	public void setOrgId(Long orgId) {
		this.orgId = orgId;
	}

	public Long getIsEmphasis() {
		return isEmphasis;
	}

	public void setIsEmphasis(Long isEmphasis) {
		this.isEmphasis = isEmphasis;
	}

	public String getOrgInternalCode() {
		return orgInternalCode;
	}

	public void setOrgInternalCode(String orgInternalCode) {
		this.orgInternalCode = orgInternalCode;
	}

	public String getVideoNo() {
		return videoNo;
	}

	public void setVideoNo(String videoNo) {
		this.videoNo = videoNo;
	}

	public String getAddress() {
		return address;
	}

	public void setAddress(String address) {
		this.address = address;
	}

	public String getLogoutReason() {
		return logoutReason;
	}

	public void setLogoutReason(String logoutReason) {
		this.logoutReason = logoutReason;
	}

	public Boolean getIsBind() {
		return isBind;
	}

	public void setIsBind(Boolean isBind) {
		this.isBind = isBind;
	}

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	public String getAccount() {
		return account;
	}

	public void setAccount(String account) {
		this.account = account;
	}

	public String getPassword() {
		return password;
	}

	public void setPassword(String password) {
		this.password = password;
	}

}

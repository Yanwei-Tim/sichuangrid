package com.tianque.core.vo;

import java.util.List;

public class ExtTreeData implements Tree {
	protected String cls;
	protected String icon;
	protected String text;
	protected boolean leaf = false;
	protected Long id;
	protected boolean expanded = false;
	protected Boolean checked = null;
	protected List<ExtTreeData> children;
	protected int level;

	public ExtTreeData() {
	}

	public String getCls() {
		return cls;
	}

	public void setCls(String cls) {
		this.cls = cls;
	}

	public String getText() {
		return text;
	}

	public void setText(String text) {
		this.text = text;
	}

	public boolean isLeaf() {
		return leaf;
	}

	public void setLeaf(boolean leaf) {
		this.leaf = leaf;
	}

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getIcon() {
		return icon;
	}

	public void setIcon(String icon) {
		this.icon = icon;
	}

	public boolean isExpanded() {
		return expanded;
	}

	public void setExpanded(boolean expanded) {
		this.expanded = expanded;
	}

	public List<ExtTreeData> getChildren() {
		return children;
	}

	public void setChildren(List<ExtTreeData> children) {
		this.children = children;
	}

	public void setChecked(Boolean checked) {
		this.checked = checked;
	}

	public int getLevel() {
		return level;
	}

	public void setLevel(int level) {
		this.level = level;
	}

	public Boolean getChecked() {
		return checked;
	}

}

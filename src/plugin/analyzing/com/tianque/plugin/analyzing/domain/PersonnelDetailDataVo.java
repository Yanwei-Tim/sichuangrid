package com.tianque.plugin.analyzing.domain;

public class PersonnelDetailDataVo {
	private String name;
	private int amount;
	private double amountPercentage;
	private int helped;
	private int noHelp;
	private int resited;
	private int noResite;
	private int recidivism;
	private boolean showLink = false;

	public boolean isShowLink() {
		return showLink;
	}

	public void setShowLink(boolean showLink) {
		this.showLink = showLink;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getAmount() {
		return amount;
	}

	public void setAmount(int amount) {
		this.amount = amount;
	}

	public int getHelped() {
		return helped;
	}

	public void setHelped(int helped) {
		this.helped = helped;
	}

	public int getNoHelp() {
		return noHelp;
	}

	public void setNoHelp(int noHelp) {
		this.noHelp = noHelp;
	}

	public int getResited() {
		return resited;
	}

	public void setResited(int resited) {
		this.resited = resited;
	}

	public int getNoResite() {
		return noResite;
	}

	public void setNoResite(int noResite) {
		this.noResite = noResite;
	}

	public int getRecidivism() {
		return recidivism;
	}

	public void setRecidivism(int recidivism) {
		this.recidivism = recidivism;
	}

	public double getAmountPercentage() {
		return amountPercentage;
	}

	public void setAmountPercentage(double amountPercentage) {
		this.amountPercentage = amountPercentage;
	}

}

package com.tianque.plugin.analyzing.domain;

import java.io.Serializable;
import java.util.List;

import com.tianque.domain.Organization;

public class PopulationAreaDataVo implements Serializable{

	private Organization org;
	private Long orgId;
	/** 区域各类人员总数 */
	private Integer amount; // 总数
	private List<PopulationDetailDataVo> populationDetailDatas;

	public Organization getOrg() {
		return org;
	}

	public void setOrg(Organization org) {
		this.org = org;
	}

	public Integer getAmount() {
		return amount;
	}

	public void setAmount(Integer amount) {
		this.amount = amount;
	}

	public List<PopulationDetailDataVo> getPopulationDetailDatas() {
		return populationDetailDatas;
	}

	public void setPopulationDetailDatas(List<PopulationDetailDataVo> populationDetailDatas) {
		this.populationDetailDatas = populationDetailDatas;
	}

	public Long getOrgId() {
		return orgId;
	}

	public void setOrgId(Long orgId) {
		this.orgId = orgId;
	}

}

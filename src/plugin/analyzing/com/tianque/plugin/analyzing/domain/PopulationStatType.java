package com.tianque.plugin.analyzing.domain;

import java.util.Date;

import com.tianque.baseInfo.youths.vo.SearchYouthsVo;
import com.tianque.core.base.BaseDomain;

public class PopulationStatType extends BaseDomain {

	/** 统计的年份 */
	private Integer year;
	/** 统计的月份 */
	private Integer month;
	/** 每类人员各自的分类的总数 */
	private Long total;
	/** 每类人员各自的分类 */
	private String typeName;
	/** 实有人口的分类类别(流动人员,户籍人员,未落户人员,境外人员) */
	private String populationType;
	private String orgInternalCode;
	/** 统计开始时间 */
	private Date startDate;
	/** 统计结束时间 */
	private Date endDate;
	/** 所占百分比 total/sum */
	private String percentage;
	/** 每类人员的总数 */
	private Long sum;
	/** 本月前所有数据 */
	private Long objectsum;
	/** 本月新增数据 */
	private Long monthcreate;
	/** 青少年查询条件 */
	private SearchYouthsVo searchYouthsVo;

	public Integer getYear() {
		return year;
	}

	public void setYear(Integer year) {
		this.year = year;
	}

	public Integer getMonth() {
		return month;
	}

	public void setMonth(Integer month) {
		this.month = month;
	}

	public Long getTotal() {
		return total;
	}

	public void setTotal(Long total) {
		this.total = total;
	}

	public String getTypeName() {
		return typeName;
	}

	public void setTypeName(String typeName) {
		this.typeName = typeName;
	}

	public String getPopulationType() {
		return populationType;
	}

	public void setPopulationType(String populationType) {
		this.populationType = populationType;
	}

	public String getOrgInternalCode() {
		return orgInternalCode;
	}

	public void setOrgInternalCode(String orgInternalCode) {
		this.orgInternalCode = orgInternalCode;
	}

	public Date getStartDate() {
		return startDate;
	}

	public void setStartDate(Date startDate) {
		this.startDate = startDate;
	}

	public Date getEndDate() {
		return endDate;
	}

	public void setEndDate(Date endDate) {
		this.endDate = endDate;
	}

	public String getPercentage() {
		return percentage;
	}

	public void setPercentage(String percentage) {
		this.percentage = percentage;
	}

	public Long getSum() {
		return sum;
	}

	public void setSum(Long sum) {
		this.sum = sum;
	}

	public Long getObjectsum() {
		return objectsum;
	}

	public void setObjectsum(Long objectsum) {
		this.objectsum = objectsum;
	}

	public Long getMonthcreate() {
		return monthcreate;
	}

	public void setMonthcreate(Long monthcreate) {
		this.monthcreate = monthcreate;
	}

	public SearchYouthsVo getSearchYouthsVo() {
		return searchYouthsVo;
	}

	public void setSearchYouthsVo(SearchYouthsVo searchYouthsVo) {
		this.searchYouthsVo = searchYouthsVo;
	}

}

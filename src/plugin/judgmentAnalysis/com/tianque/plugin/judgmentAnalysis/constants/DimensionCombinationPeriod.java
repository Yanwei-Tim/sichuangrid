package com.tianque.plugin.judgmentAnalysis.constants;

public enum DimensionCombinationPeriod {
	MONTH("月","month"),YEAR("年","year"),QUARTER("季度","quarter");
	
	public String name;
	public String value;
		
	private DimensionCombinationPeriod() {
	}
	
	private DimensionCombinationPeriod(String name,String value) {
		this.name = name;
		this.value = value;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getValue() {
		return value;
	}

	public void setValue(String value) {
		this.value = value;
	}
	
	
}

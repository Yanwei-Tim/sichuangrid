package com.tianque.plugin.judgmentAnalysis.vo;

import java.io.Serializable;

public class EmphasisIsHelpHistoryVo implements Serializable {

	private static final long serialVersionUID = 1L;

	private Long orgId;
	private String orgName;

	private Long amount;// 总数
	private Long isHelp_amount; // 已走访
	private Long noHelp_amount; // 未走访
	private Long help_rate; // 走访率
	private Long isDone_amount; // 已落实
	private Long noDone_amount; // 未落实
	private Long done_rate; // 落实率

	public Long getOrgId() {
		return orgId;
	}

	public void setOrgId(Long orgId) {
		this.orgId = orgId;
	}

	public String getOrgName() {
		return orgName;
	}

	public void setOrgName(String orgName) {
		this.orgName = orgName;
	}

	public Long getIsHelp_amount() {
		return isHelp_amount;
	}

	public void setIsHelp_amount(Long isHelpAmount) {
		isHelp_amount = isHelpAmount;
	}

	public Long getNoHelp_amount() {
		return noHelp_amount;
	}

	public void setNoHelp_amount(Long noHelpAmount) {
		noHelp_amount = noHelpAmount;
	}

	public Long getHelp_rate() {
		return help_rate;
	}

	public void setHelp_rate(Long helpRate) {
		help_rate = helpRate;
	}

	public Long getIsDone_amount() {
		return isDone_amount;
	}

	public void setIsDone_amount(Long isDoneAmount) {
		isDone_amount = isDoneAmount;
	}

	public Long getDone_rate() {
		return done_rate;
	}

	public void setDone_rate(Long doneRate) {
		done_rate = doneRate;
	}

	public Long getAmount() {
		return amount;
	}

	public void setAmount(Long amount) {
		this.amount = amount;
	}

	public Long getNoDone_amount() {
		return noDone_amount;
	}

	public void setNoDone_amount(Long noDoneAmount) {
		noDone_amount = noDoneAmount;
	}

}

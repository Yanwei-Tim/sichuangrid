package com.tianque.newVillage.domain;

import com.tianque.core.base.BaseDomain;

/**
 * 评分规则实体类
 * 
 * @author szq
 * 
 */
public class ScoringRules extends BaseDomain {

	private String correspondingValue;// 对应字段名

	private Integer calculationSymbol;// 计算符

	private Double scoringRangeStart;// 范围值始

	private Double scoringRangeEnd;// 范围值末

	private Integer fraction;// 分数

	public String getCorrespondingValue() {
		return correspondingValue;
	}

	public void setCorrespondingValue(String correspondingValue) {
		this.correspondingValue = correspondingValue;
	}

	public Integer getCalculationSymbol() {
		return calculationSymbol;
	}

	public void setCalculationSymbol(Integer calculationSymbol) {
		this.calculationSymbol = calculationSymbol;
	}

	public Double getScoringRangeStart() {
		return scoringRangeStart;
	}

	public void setScoringRangeStart(Double scoringRangeStart) {
		this.scoringRangeStart = scoringRangeStart;
	}

	public Double getScoringRangeEnd() {
		return scoringRangeEnd;
	}

	public void setScoringRangeEnd(Double scoringRangeEnd) {
		this.scoringRangeEnd = scoringRangeEnd;
	}

	public Integer getFraction() {
		return fraction;
	}

	public void setFraction(Integer fraction) {
		this.fraction = fraction;
	}

}

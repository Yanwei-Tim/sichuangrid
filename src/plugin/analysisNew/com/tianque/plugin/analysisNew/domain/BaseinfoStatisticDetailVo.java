package com.tianque.plugin.analysisNew.domain;

import java.io.Serializable;

/**
 * 统计的明细信息
 * 
 * @author
 */
public class BaseinfoStatisticDetailVo implements Serializable {
	private long orgId;// 组织机构Id
	private String orgName;// 组织机构名称
	private String typeName;// 分类的名称
	private long sum;// 本组详细信息的总数量
	private long helped;// 已帮教数量
	private long noHelp;
	private long resited;// 犯罪数量
	private long noResite; // 未犯罪数量
	private long recidivism;// 已安置
	private long noRecidivism;// 未安置
	private String countField;// 要统计的字段
	private long countValue;// 统计的字段值
	private boolean showLink = false;// 是否显示超链接

	private double amountPercentage; // 百分比

	private Long monthCreate;// 本月新增的数据数量

	public String getOrgName() {
		return orgName;
	}

	public void setOrgName(String orgName) {
		this.orgName = orgName;
	}

	public String getTypeName() {
		return typeName;
	}

	public void setTypeName(String typeName) {
		this.typeName = typeName;
	}

	public long getHelped() {
		return helped;
	}

	public void setHelped(long helped) {
		this.helped = helped;
	}

	public long getNoHelp() {
		return noHelp;
	}

	public void setNoHelp(long noHelp) {
		this.noHelp = noHelp;
	}

	public long getResited() {
		return resited;
	}

	public void setResited(long resited) {
		this.resited = resited;
	}

	public long getNoResite() {
		return noResite;
	}

	public void setNoResite(long noResite) {
		this.noResite = noResite;
	}

	public long getRecidivism() {
		return recidivism;
	}

	public void setRecidivism(long recidivism) {
		this.recidivism = recidivism;
	}

	public boolean isShowLink() {
		return showLink;
	}

	public void setShowLink(boolean showLink) {
		this.showLink = showLink;
	}

	public String getCountField() {
		return countField;
	}

	public void setCountField(String countField) {
		this.countField = countField;
	}

	public long getCountValue() {
		return countValue;
	}

	public void setCountValue(long countValue) {
		this.countValue = countValue;
	}

	public long getSum() {
		return sum;
	}

	public void setSum(long sum) {
		this.sum = sum;
	}

	public long getOrgId() {
		return orgId;
	}

	public void setOrgId(long orgId) {
		this.orgId = orgId;
	}

	public double getAmountPercentage() {
		return amountPercentage;
	}

	public void setAmountPercentage(double amountPercentage) {
		this.amountPercentage = amountPercentage;
	}

	public long getNoRecidivism() {
		return noRecidivism;
	}

	public void setNoRecidivism(long noRecidivism) {
		this.noRecidivism = noRecidivism;
	}

	public Long getMonthCreate() {
		return monthCreate;
	}

	public void setMonthCreate(Long monthCreate) {
		this.monthCreate = monthCreate;
	}
}

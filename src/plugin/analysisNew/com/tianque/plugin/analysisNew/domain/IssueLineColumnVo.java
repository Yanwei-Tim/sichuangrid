package com.tianque.plugin.analysisNew.domain;

import java.io.Serializable;
import java.util.List;

public class IssueLineColumnVo implements Serializable {
	private String moduleName; // 模块名
	private String[] categories; // 部门数组
	private List<IssueLineDataColumnVo> series; // 各类型对应的数据

	public String[] getCategories() {
		return categories;
	}

	public void setCategories(String[] categories) {
		this.categories = categories;
	}

	public String getModuleName() {
		return moduleName;
	}

	public void setModuleName(String moduleName) {
		this.moduleName = moduleName;
	}

	public void setSeries(List<IssueLineDataColumnVo> series) {
		this.series = series;
	}

	public List<IssueLineDataColumnVo> getSeries() {
		return series;
	}
}

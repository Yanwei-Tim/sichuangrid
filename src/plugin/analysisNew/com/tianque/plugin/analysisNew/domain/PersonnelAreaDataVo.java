package com.tianque.plugin.analysisNew.domain;

import java.io.Serializable;
import java.util.List;

import com.tianque.domain.Organization;

public class PersonnelAreaDataVo implements Serializable {
	private Organization org;
	private int amount; // 总数
	private List<PersonnelDetailDataVo> personnelDetailDatas;

	public Organization getOrg() {
		return org;
	}

	public void setOrg(Organization org) {
		this.org = org;
	}

	public int getAmount() {
		return amount;
	}

	public void setAmount(int amount) {
		this.amount = amount;
	}

	public List<PersonnelDetailDataVo> getPersonnelDetailDatas() {
		return personnelDetailDatas;
	}

	public void setPersonnelDetailDatas(
			List<PersonnelDetailDataVo> personnelDetailDatas) {
		this.personnelDetailDatas = personnelDetailDatas;
	}

}

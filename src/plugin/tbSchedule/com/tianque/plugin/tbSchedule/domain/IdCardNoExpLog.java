package com.tianque.plugin.tbSchedule.domain;

import com.tianque.core.base.BaseDomain;

public class IdCardNoExpLog extends BaseDomain {
	private String expIdCardNo;
	private String expMessage;

	public IdCardNoExpLog() {
	}

	public IdCardNoExpLog(String expIdCardNo, String expMessage) {
		super();
		this.expIdCardNo = expIdCardNo;
		this.expMessage = expMessage;
	}

	public String getExpIdCardNo() {
		return expIdCardNo;
	}

	public void setExpIdCardNo(String expIdCardNo) {
		this.expIdCardNo = expIdCardNo;
	}

	public String getExpMessage() {
		return expMessage;
	}

	public void setExpMessage(String expMessage) {
		this.expMessage = expMessage;
	}

}

package com.tianque.openLayersMap.positioningTrajectory.domian.vo;

import java.util.Date;

/**
 * 定位轨迹信息查询vo
 */
public class SearchPositioningTrajectoryVo {
	/** 定位时间 */
	private Date locateDateStart;
	/** 定位时间 */
	private Date locateDateEnd;
	/** 设备编号 */
	private String deviceNumber;
	/** 用户名 */
	private String userName;

	public Date getLocateDateStart() {
		return locateDateStart;
	}

	public void setLocateDateStart(Date locateDateStart) {
		this.locateDateStart = locateDateStart;
	}

	public Date getLocateDateEnd() {
		return locateDateEnd;
	}

	public void setLocateDateEnd(Date locateDateEnd) {
		this.locateDateEnd = locateDateEnd;
	}

	public String getDeviceNumber() {
		return deviceNumber;
	}

	public void setDeviceNumber(String deviceNumber) {
		this.deviceNumber = deviceNumber;
	}

	public String getUserName() {
		return userName;
	}

	public void setUserName(String userName) {
		this.userName = userName;
	}

}

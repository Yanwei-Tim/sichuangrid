package com.tianque.openLayersMap.domian.vo;

import com.tianque.core.base.BaseDomain;
import com.tianque.domain.Organization;

/**
 * 二维地图 划区域统计信息的实体类
 * 
 * @author
 * 
 */
public class JurisdictionStatisticVo extends BaseDomain {
	private static final long	serialVersionUID	= 1L;

	// 所属辖区
	private Organization		organization;
	// 人口总数
	private Integer				populationSum;
	// 常住人口数量
	private Integer				householdStaffSum;
	// 流动人口数量
	private Integer				floatingPopulationSum;
	// 境外人员数量
	private Integer				overseaStaffSum;
	// 未落户人员数量
	private Integer				unsettledPopulationSum;
	// 重点人员数量
	private Integer				keyPersonSum;
	// 关怀对象数量
	private Integer				carePersonSum;
	// 其他关注对象数量
	private Integer				otherPersonSum;
	// 组织场所数量
	private Integer				orgLocationSum;
	// 房屋数量
	private Integer				houseSum;

	public Organization getOrganization() {
		return organization;
	}

	public void setOrganization(Organization organization) {
		this.organization = organization;
	}

	public Integer getPopulationSum() {
		return populationSum;
	}

	public void setPopulationSum(Integer populationSum) {
		this.populationSum = populationSum;
	}

	public Integer getHouseholdStaffSum() {
		return householdStaffSum;
	}

	public void setHouseholdStaffSum(Integer householdStaffSum) {
		this.householdStaffSum = householdStaffSum;
	}

	public Integer getFloatingPopulationSum() {
		return floatingPopulationSum;
	}

	public void setFloatingPopulationSum(Integer floatingPopulationSum) {
		this.floatingPopulationSum = floatingPopulationSum;
	}

	public Integer getOverseaStaffSum() {
		return overseaStaffSum;
	}

	public void setOverseaStaffSum(Integer overseaStaffSum) {
		this.overseaStaffSum = overseaStaffSum;
	}

	public Integer getUnsettledPopulationSum() {
		return unsettledPopulationSum;
	}

	public void setUnsettledPopulationSum(Integer unsettledPopulationSum) {
		this.unsettledPopulationSum = unsettledPopulationSum;
	}

	public Integer getKeyPersonSum() {
		return keyPersonSum;
	}

	public void setKeyPersonSum(Integer keyPersonSum) {
		this.keyPersonSum = keyPersonSum;
	}

	public Integer getCarePersonSum() {
		return carePersonSum;
	}

	public void setCarePersonSum(Integer carePersonSum) {
		this.carePersonSum = carePersonSum;
	}

	public Integer getOtherPersonSum() {
		return otherPersonSum;
	}

	public void setOtherPersonSum(Integer otherPersonSum) {
		this.otherPersonSum = otherPersonSum;
	}

	public Integer getOrgLocationSum() {
		return orgLocationSum;
	}

	public void setOrgLocationSum(Integer orgLocationSum) {
		this.orgLocationSum = orgLocationSum;
	}

	public Integer getHouseSum() {
		return houseSum;
	}

	public void setHouseSum(Integer houseSum) {
		this.houseSum = houseSum;
	}

}

package com.tianque.openLayersMap.domian;



/**
 * 子类类别管理对象类
 * 
 */
@SuppressWarnings("serial")
public class GisTypeManage extends PermissionVo {
	/** 对象名称 */
	private String name;
	/** 表名 */
	private String tableName;
	/** 类型 */
	private String key;
	/** 父类类型名称 */
	private String innerName;
	/** 父类类型 */
	private String innerKey;
	/** 选中状态 */
	private Boolean checked;
	/** 所属网格字段 */
	private String orgFiled;
	/** 原表表名（分表之前） */
	private String originalTableName;

	public String getTableName() {
		return tableName;
	}

	public void setTableName(String tableName) {
		this.tableName = tableName;
	}

	public Boolean getChecked() {
		return checked;
	}

	public void setChecked(Boolean checked) {
		this.checked = checked;
	}

	public String getInnerName() {
		return innerName;
	}

	public void setInnerName(String innerName) {
		this.innerName = innerName;
	}

	public String getInnerKey() {
		return innerKey;
	}

	public void setInnerKey(String innerKey) {
		this.innerKey = innerKey;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getKey() {
		return key;
	}

	public void setKey(String key) {
		this.key = key;
	}

	public String getOrgFiled() {
		return orgFiled;
	}

	public void setOrgFiled(String orgFiled) {
		this.orgFiled = orgFiled;
	}

	public String getOriginalTableName() {
		return originalTableName;
	}

	public void setOriginalTableName(String originalTableName) {
		this.originalTableName = originalTableName;
	}

}

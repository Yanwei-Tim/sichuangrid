package com.tianque.openLayersMap.service.impl;


import com.tianque.openLayersMap.domian.Gis2DLayer;
import com.tianque.openLayersMap.domian.vo.SearchInfoVo;

public abstract class Callback {
	protected Object tempParam;

	public Callback() {
		super();
	}

	public Callback(Object tempParam) {
		super();
		this.tempParam = tempParam;
	}
	
	public abstract Integer call(Gis2DLayer gis2dLayer, SearchInfoVo searchVo);
	
}

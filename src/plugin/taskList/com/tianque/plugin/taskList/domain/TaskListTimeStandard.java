package com.tianque.plugin.taskList.domain;

import com.tianque.core.base.BaseDomain;
import com.tianque.domain.Organization;
import com.tianque.domain.PropertyDict;

/**
 * Created by hesimin on 2015/12/17.
 */
public class TaskListTimeStandard extends BaseDomain{
	// 职能部门组织
	private Organization org;
	private PropertyDict itemNameDict;
	private Integer signYellowLimit;
	private Integer signRedLimit;
	private Integer replayYellowLimit;
	private Integer replayRedLimit;
	private String remark;

	private Long sysCurrTime;//系统当前时间，页面上亮牌时间标准
	private String itemNameIds;
	public Organization getOrg() {
		return org;
	}

	public void setOrg(Organization org) {
		this.org = org;
	}

	public PropertyDict getItemNameDict() {
		return itemNameDict;
	}

	public void setItemNameDict(PropertyDict itemNameDict) {
		this.itemNameDict = itemNameDict;
	}

	public Integer getSignYellowLimit() {
		return signYellowLimit;
	}

	public void setSignYellowLimit(Integer signYellowLimit) {
		this.signYellowLimit = signYellowLimit;
	}

	public Integer getSignRedLimit() {
		return signRedLimit;
	}

	public void setSignRedLimit(Integer signRedLimit) {
		this.signRedLimit = signRedLimit;
	}

	public Integer getReplayYellowLimit() {
		return replayYellowLimit;
	}

	public void setReplayYellowLimit(Integer replayYellowLimit) {
		this.replayYellowLimit = replayYellowLimit;
	}

	public Integer getReplayRedLimit() {
		return replayRedLimit;
	}

	public void setReplayRedLimit(Integer replayRedLimit) {
		this.replayRedLimit = replayRedLimit;
	}

	public String getRemark() {
		return remark;
	}

	public void setRemark(String remark) {
		this.remark = remark;
	}

	public String getItemNameIds() {
		return itemNameIds;
	}

	public void setItemNameIds(String itemNameIds) {
		this.itemNameIds = itemNameIds;
	}

	public Long getSysCurrTime() {
		return sysCurrTime;
	}

	public void setSysCurrTime(Long sysCurrTime) {
		this.sysCurrTime = sysCurrTime;
	}
}

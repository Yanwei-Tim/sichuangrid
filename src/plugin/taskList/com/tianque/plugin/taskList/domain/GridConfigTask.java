package com.tianque.plugin.taskList.domain;

import com.tianque.core.base.BaseDomain;
import com.tianque.domain.Organization;

/**
 * 网格员配置实体类
 * 
 * @author weiminglong
 * 
 */
public class GridConfigTask extends BaseDomain {

	/**
	 * 职能部门
	 */
	private Long funOrgId;
	/**
	 * 行政部门
	 */
	private Long areaOrgId;
	/**
	 * 行政级别
	 */
	private Long areaOrgLevel;
	/**
	 * 上级部门
	 */
	private Long areaParentId;
	
	/**
	 * 组织机构
	 */
	private Organization organization;
    
	private String orgInternalCode;

	private Long ishandle;

	private String advice;
	
	
	public Long getFunOrgId() {
		return funOrgId;
	}
	public void setFunOrgId(Long funOrgId) {
		this.funOrgId = funOrgId;
	}
	public Long getAreaOrgId() {
		return areaOrgId;
	}
	public void setAreaOrgId(Long areaOrgId) {
		this.areaOrgId = areaOrgId;
	}
	public Long getAreaOrgLevel() {
		return areaOrgLevel;
	}
	public void setAreaOrgLevel(Long areaOrgLevel) {
		this.areaOrgLevel = areaOrgLevel;
	}
	public Long getAreaParentId() {
		return areaParentId;
	}
	public void setAreaParentId(Long areaParentId) {
		this.areaParentId = areaParentId;
	}
	public Organization getOrganization() {
		return organization;
	}
	public void setOrganization(Organization organization) {
		this.organization = organization;
	}
	public String getOrgInternalCode() {
		return orgInternalCode;
	}
	public void setOrgInternalCode(String orgInternalCode) {
		this.orgInternalCode = orgInternalCode;
	}
	public Long getIshandle() {
		return ishandle;
	}
	public void setIshandle(Long ishandle) {
		this.ishandle = ishandle;
	}
	public String getAdvice() {
		return advice;
	}
	public void setAdvice(String advice) {
		this.advice = advice;
	}
	

}

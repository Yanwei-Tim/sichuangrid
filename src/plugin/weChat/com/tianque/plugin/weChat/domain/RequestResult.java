package com.tianque.plugin.weChat.domain;

/**
 * 用来统一返回结果
 * @ClassName: Result 
 * @author: he.simin
 * @date: 2015年11月9日 下午2:49:47
 */
public class RequestResult {
	private String errorCode = "0";
	private String message;
	private Object data;

	public String getErrorCode() {
		return errorCode;
	}

	public RequestResult setErrorCode(String errorCode) {
		this.errorCode = errorCode;
		return this;
	}

	public String getMessage() {
		return message;
	}

	public RequestResult setMessage(String message) {
		this.message = message;
		return this;
	}

	public Object getData() {
		return data;
	}

	public RequestResult setData(Object data) {
		this.data = data;
		return this;
	}

}

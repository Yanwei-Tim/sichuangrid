package com.tianque.plugin.weChat.domain.inbox;


import com.tianque.core.base.BaseDomain;
import com.tianque.domain.Organization;
import com.tianque.domain.PropertyDict;

public class WechatInboxVoicePrompt extends BaseDomain{

    /**信息所属组织机构*/
    private Organization org;
    /**微信公众号*/
    private String toUserName;  
    /**语音提示状态*/
    private PropertyDict voicePromptStatus;
    /**语音路径*/
    private String voiceUrl;
    /**是否有新的未读消息(0为否,1为是)*/
    private Long isNewInbox;
	public Organization getOrg() {
		return org;
	}
	public void setOrg(Organization org) {
		this.org = org;
	}
	public String getToUserName() {
		return toUserName;
	}
	public void setToUserName(String toUserName) {
		this.toUserName = toUserName;
	}
	public PropertyDict getVoicePromptStatus() {
		return voicePromptStatus;
	}
	public void setVoicePromptStatus(PropertyDict voicePromptStatus) {
		this.voicePromptStatus = voicePromptStatus;
	}
	public String getVoiceUrl() {
		return voiceUrl;
	}
	public void setVoiceUrl(String voiceUrl) {
		this.voiceUrl = voiceUrl;
	}
	public Long getIsNewInbox() {
		return isNewInbox;
	}
	public void setIsNewInbox(Long isNewInbox) {
		this.isNewInbox = isNewInbox;
	}
}

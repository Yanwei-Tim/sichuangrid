package com.tianque.plugin.weChat.domain.menu;

/**
 * 普通按钮（单击按钮）
 * @author 
 *  @date  2014年4月17日
 */
public class ClickButton extends Button {

	private String type;
	private String key;

	public ClickButton() {
	}

	public ClickButton(String name, String key, String type) {
		super.setName(name);
		this.key = key;
		this.type = type;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getKey() {
		return key;
	}

	public void setKey(String key) {
		this.key = key;
	}

}

package com.tianque.plugin.weChat.domain.user;

import com.tianque.core.base.BaseDomain;

public class WeChatSourceAttachment extends BaseDomain {

	/**主键id*/
	private Long attachmentId;
	/**素材信息id*/
	private Long sourceId;
	/**文件大小*/
	private Long fileSize;
	/**文件扩展名*/
	private String extFileName;
	/**文件显示名*/
	private String fileName;
	/**文件路径*/
	private String fileActualUrl;

	private String weChatUserId;

	public String getWeChatUserId() {
		return weChatUserId;
	}

	public void setWeChatUserId(String weChatUserId) {
		this.weChatUserId = weChatUserId;
	}

	public Long getAttachmentId() {
		return attachmentId;
	}

	public void setAttachmentId(Long attachmentId) {
		this.attachmentId = attachmentId;
	}

	public Long getSourceId() {
		return sourceId;
	}

	public void setSourceId(Long sourceId) {
		this.sourceId = sourceId;
	}

	public Long getFileSize() {
		return fileSize;
	}

	public void setFileSize(Long fileSize) {
		this.fileSize = fileSize;
	}

	public String getExtFileName() {
		return extFileName;
	}

	public void setExtFileName(String extFileName) {
		this.extFileName = extFileName;
	}

	public String getFileName() {
		return fileName;
	}

	public void setFileName(String fileName) {
		this.fileName = fileName;
	}

	public String getFileActualUrl() {
		return fileActualUrl;
	}

	public void setFileActualUrl(String fileActualUrl) {
		this.fileActualUrl = fileActualUrl;
	}

}

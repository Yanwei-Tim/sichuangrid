package com.tianque.plugin.account.domain;

import java.util.Date;

import org.apache.struts2.json.annotations.JSON;

import com.tianque.core.base.BaseDomain;
import com.tianque.domain.PropertyDict;

public class LedgerSteadyWorkPeopleInfo extends BaseDomain {

	private LedgerSteadyWork ledgerSteadyWork;
	private String name;
	private String idCardNo;
	private String mobileNumber;
	private PropertyDict gender;
	private Date birthDay;
	private String permanentAddress;
	private Boolean partyMember;
	private PropertyDict position;

	public LedgerSteadyWork getLedgerSteadyWork() {
		return ledgerSteadyWork;
	}

	public void setLedgerSteadyWork(LedgerSteadyWork ledgerSteadyWork) {
		this.ledgerSteadyWork = ledgerSteadyWork;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getIdCardNo() {
		return idCardNo;
	}

	public void setIdCardNo(String idCardNo) {
		this.idCardNo = idCardNo;
	}

	public String getMobileNumber() {
		return mobileNumber;
	}

	public void setMobileNumber(String mobileNumber) {
		this.mobileNumber = mobileNumber;
	}

	public PropertyDict getGender() {
		return gender;
	}

	public void setGender(PropertyDict gender) {
		this.gender = gender;
	}

	@JSON(format = "yyyy-MM-dd")
	public Date getBirthDay() {
		return birthDay;
	}

	public void setBirthDay(Date birthDay) {
		this.birthDay = birthDay;
	}

	public String getPermanentAddress() {
		return permanentAddress;
	}

	public void setPermanentAddress(String permanentAddress) {
		this.permanentAddress = permanentAddress;
	}

	public Boolean getPartyMember() {
		return partyMember;
	}

	public void setPartyMember(Boolean partyMember) {
		this.partyMember = partyMember;
	}

	public PropertyDict getPosition() {
		return position;
	}

	public void setPosition(PropertyDict position) {
		this.position = position;
	}

}

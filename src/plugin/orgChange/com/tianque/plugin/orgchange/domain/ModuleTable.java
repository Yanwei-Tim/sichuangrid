package com.tianque.plugin.orgchange.domain;

import com.tianque.core.base.BaseDomain;
import com.tianque.domain.Permission;

/**
 * 功能模块表信息
 * 
 * @author 曾利民<br>
 *         email:zenglimin@hztianque.com <br>
 *         date:2014年9月23日
 */
public class ModuleTable extends BaseDomain {

	private Permission permission;

	private Boolean active;

	private String tableName;

	/** 是否分表 */
	private Boolean split;

	private String orgIdColumn;

	private String orgCodeColumn;

	private int executeType;

	private Long isMainTable;

	private int executeLevel;

	private int operateMode;

	private String countScript;

	private String selectScript;

	private String updateScript;

	private String deleteScript;

	private String beanName;

	public String getBeanName() {
		return beanName;
	}

	public void setBeanName(String beanName) {
		this.beanName = beanName;
	}

	public String getDeleteScript() {
		return deleteScript;
	}

	public void setDeleteScript(String deleteScript) {
		this.deleteScript = deleteScript;
	}

	public Permission getPermission() {
		return permission;
	}

	public void setPermission(Permission permission) {
		this.permission = permission;
	}

	public Boolean getActive() {
		return active;
	}

	public void setActive(Boolean active) {
		this.active = active;
	}

	public String getTableName() {
		return tableName;
	}

	public void setTableName(String tableName) {
		this.tableName = tableName;
	}

	public String getOrgIdColumn() {
		return orgIdColumn;
	}

	public void setOrgIdColumn(String orgIdColumn) {
		this.orgIdColumn = orgIdColumn;
	}

	public String getOrgCodeColumn() {
		return orgCodeColumn;
	}

	public void setOrgCodeColumn(String orgCodeColumn) {
		this.orgCodeColumn = orgCodeColumn;
	}

	public int getExecuteType() {
		return executeType;
	}

	public void setExecuteType(int executeType) {
		this.executeType = executeType;
	}

	public Long getIsMainTable() {
		return isMainTable;
	}

	public void setIsMainTable(Long isMainTable) {
		this.isMainTable = isMainTable;
	}

	public int getExecuteLevel() {
		return executeLevel;
	}

	public void setExecuteLevel(int executeLevel) {
		this.executeLevel = executeLevel;
	}

	public int getOperateMode() {
		return operateMode;
	}

	public void setOperateMode(int operateMode) {
		this.operateMode = operateMode;
	}

	public String getCountScript() {
		return countScript;
	}

	public void setCountScript(String countScript) {
		this.countScript = countScript;
	}

	public String getSelectScript() {
		return selectScript;
	}

	public void setSelectScript(String selectScript) {
		this.selectScript = selectScript;
	}

	public String getUpdateScript() {
		return updateScript;
	}

	public void setUpdateScript(String updateScript) {
		this.updateScript = updateScript;
	}

	@Override
	public String toString() {
		return "ModuleTable [permission=" + permission + ", active=" + active
				+ ", tableName=" + tableName + ", orgIdColumn=" + orgIdColumn
				+ ", split=" + split + ", orgCodeColumn=" + orgCodeColumn
				+ ", executeType=" + executeType + ", isMainTable="
				+ isMainTable + ", executeLevel=" + executeLevel
				+ ", operateMode=" + operateMode + ", countScript="
				+ countScript + ", selectScript=" + selectScript
				+ ", updateScript=" + updateScript + ", deleteScript="
				+ deleteScript + ", getId()=" + getId() + "]";
	}

	public Boolean isSplit() {
		return split;
	}

	public void setSplit(Boolean split) {
		this.split = split;
	}

}

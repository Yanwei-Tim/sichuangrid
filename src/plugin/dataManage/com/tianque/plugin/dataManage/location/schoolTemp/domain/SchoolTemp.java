package com.tianque.plugin.dataManage.location.schoolTemp.domain;

import com.tianque.domain.PropertyDict;
import com.tianque.plugin.dataManage.base.vo.ClaimDetail;
import com.tianque.plugin.dataManage.location.placeCommon.domain.PlaceCommonTemp;

/**
 * 学校
 */
@SuppressWarnings("serial")
public class SchoolTemp extends PlaceCommonTemp {
	private ClaimDetail claimDetail;
	/** 学校性质 */
	private PropertyDict kind;
	/** 学校类型 */
	private PropertyDict type;
	/** 法制副校长 */
	private String vicePresident;
	/** 联系电话 */
	private String telephone;
	/** 联系手机 */
	private String mobileNumber;
	/** 学校网址 */
	private String webSite;
	/** 在校人数 */
	private Long atSchoolHeadcount;
	/** 英文名称 */
	private String englishName;
	/** 传真 */
	private String fax;
	/** 电子邮箱 */
	private String email;

	public PropertyDict getKind() {
		return kind;
	}

	public void setKind(PropertyDict kind) {
		this.kind = kind;
	}

	public PropertyDict getType() {
		return type;
	}

	public void setType(PropertyDict type) {
		this.type = type;
	}

	public String getVicePresident() {
		return vicePresident;
	}

	public void setVicePresident(String vicePresident) {
		this.vicePresident = vicePresident;
	}

	public String getTelephone() {
		return telephone;
	}

	public void setTelephone(String telephone) {
		this.telephone = telephone;
	}

	public String getMobileNumber() {
		return mobileNumber;
	}

	public void setMobileNumber(String mobileNumber) {
		this.mobileNumber = mobileNumber;
	}

	public String getWebSite() {
		return webSite;
	}

	public void setWebSite(String webSite) {
		this.webSite = webSite;
	}

	public Long getAtSchoolHeadcount() {
		return atSchoolHeadcount;
	}

	public void setAtSchoolHeadcount(Long atSchoolHeadcount) {
		this.atSchoolHeadcount = atSchoolHeadcount;
	}

	public String getEnglishName() {
		return englishName;
	}

	public void setEnglishName(String englishName) {
		this.englishName = englishName;
	}

	public String getFax() {
		return fax;
	}

	public void setFax(String fax) {
		this.fax = fax;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public ClaimDetail getClaimDetail() {
		if (null == claimDetail) {
			claimDetail = new ClaimDetail();
		}
		return claimDetail;
	}

	public void setClaimDetail(ClaimDetail claimDetail) {
		this.claimDetail = claimDetail;
	}

}

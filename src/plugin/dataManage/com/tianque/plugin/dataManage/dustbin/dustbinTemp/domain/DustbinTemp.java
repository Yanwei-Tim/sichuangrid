package com.tianque.plugin.dataManage.dustbin.dustbinTemp.domain;

import java.util.Date;

import com.tianque.core.base.BaseDomain;
import com.tianque.domain.Organization;
import com.tianque.domain.PropertyDict;
import com.tianque.plugin.dataManage.base.vo.ClaimDetail;

public class DustbinTemp extends BaseDomain {
	private ClaimDetail claimDetail;
	/** 所属网格 */
	private Organization organization;
	/** 所属责任区编号 */
	private String orgInternalCode;
	/** 部件类型 */
	private PropertyDict partType;
	/** 部件名称 */
	private PropertyDict partName;
	/** 部件标识码 */
	private String dustbinCode;
	/** 主管部门 */
	private String deptName;
	/** 是否有视频流 */
	private boolean hasVideo;
	/** 权属单位 */
	private String ownershipUnitName;
	/** 养护单位 */
	private String maintenanceUnitName;
	/** 地址 */
	private String address;
	/** 经度 */
	private Double centerX;
	/** 纬度 */
	private Double centerY;
	/** 图像路径 */
	private String imgUrl;
	/** 备注 */
	private String remark;
	/** 注销时间 */
	private Date logOutTime;
	/** 注销原因 */
	private String logOutReason;
	/** 是否注销,true表示是 */
	private Boolean isEmphasis;
	/** 距离 */
	public String distance;

	public String getDistance() {
		return distance;
	}

	public void setDistance(String distance) {
		this.distance = distance;
	}

	public String getRemark() {
		return remark;
	}

	public void setRemark(String remark) {
		this.remark = remark;
	}

	public Date getLogOutTime() {
		return logOutTime;
	}

	public void setLogOutTime(Date logOutTime) {
		this.logOutTime = logOutTime;
	}

	public String getLogOutReason() {
		return logOutReason;
	}

	public void setLogOutReason(String logOutReason) {
		this.logOutReason = logOutReason;
	}

	public Boolean getIsEmphasis() {
		return isEmphasis;
	}

	public void setIsEmphasis(Boolean isEmphasis) {
		this.isEmphasis = isEmphasis;
	}

	public Organization getOrganization() {
		return organization;
	}

	public void setOrganization(Organization organization) {
		this.organization = organization;
	}

	public String getOrgInternalCode() {
		return orgInternalCode;
	}

	public void setOrgInternalCode(String orgInternalCode) {
		this.orgInternalCode = orgInternalCode;
	}

	public String getDustbinCode() {
		return dustbinCode;
	}

	public void setDustbinCode(String dustbinCode) {
		this.dustbinCode = dustbinCode;
	}

	public PropertyDict getPartType() {
		return partType;
	}

	public void setPartType(PropertyDict partType) {
		this.partType = partType;
	}

	public PropertyDict getPartName() {
		return partName;
	}

	public void setPartName(PropertyDict partName) {
		this.partName = partName;
	}

	public String getAddress() {
		return address;
	}

	public void setAddress(String address) {
		this.address = address;
	}

	public String getDeptName() {
		return deptName;
	}

	public void setDeptName(String deptName) {
		this.deptName = deptName;
	}

	public String getOwnershipUnitName() {
		return ownershipUnitName;
	}

	public void setOwnershipUnitName(String ownershipUnitName) {
		this.ownershipUnitName = ownershipUnitName;
	}

	public String getMaintenanceUnitName() {
		return maintenanceUnitName;
	}

	public void setMaintenanceUnitName(String maintenanceUnitName) {
		this.maintenanceUnitName = maintenanceUnitName;
	}

	public Double getCenterX() {
		return centerX;
	}

	public void setCenterX(Double centerX) {
		this.centerX = centerX;
	}

	public Double getCenterY() {
		return centerY;
	}

	public void setCenterY(Double centerY) {
		this.centerY = centerY;
	}

	public String getImgUrl() {
		return imgUrl;
	}

	public void setImgUrl(String imgUrl) {
		this.imgUrl = imgUrl;
	}

	public boolean isHasVideo() {
		return hasVideo;
	}

	public void setHasVideo(boolean hasVideo) {
		this.hasVideo = hasVideo;
	}

	public ClaimDetail getClaimDetail() {
		if (null == claimDetail) {
			claimDetail = new ClaimDetail();
		}
		return claimDetail;
	}

	public void setClaimDetail(ClaimDetail claimDetail) {
		this.claimDetail = claimDetail;
	}
}
